        <div class="col-md-9">For members with read access it will be a read only field.
            <ul>
                <li>Plan and track activities provide: Actual time remaining for completing an activity is captured continuously</li>
                <li>Progress of an activity and the time reported on an activity</li>
                <li>Signals if an activity is deviating from its estimates</li>
                <li>Total estimated, reported, and time remaining at the project level</li>
            </ul>
            Learn more about how you can <a href="https://help.projectplace.com/wiki/index.php/Time_on_Activities" target="_blank">plan and follow up on the time needed to complete activities</a> in the Plan.

