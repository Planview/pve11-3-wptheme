            <h2>New Plan Functionality</h2>
            <h3>Access rights for Plan</h3> Continuing to improve planning, the latest release of Projectplace gives project managers and admin the ability to set read-only access for the Plan. Share your plan with any number of people and feel confident that only certain people are allowed to make changes to it. The feature is accessible for administrators from the "settings" cog wheel in plan. Read access means that you can:
            <ul>
                <li>View activities</li>
                <li>View project steps</li>
                <li>View dependencies</li>
                <li>Add comments</li>
                <li>But not add or edit activities, project steps, or dependencies.</li>
            </ul>
            <a href="https://help.projectplace.com/wiki/index.php/Access_to_Tools_-_Overview" target="_blank">Learn more about access rights</a>

        </div>
    </div>
    <div class="row">
        <div class="col-md-12">
            <h3>Plan and track activities more effectively</h3> Projectplace introduces the concept of "estimated time", "reported time" and "remaining time" on an activity level. Time is entered on the lowest level of the Work Breakdown Structure (WBS) in the Gantt Plan and is automatically aggregated to higher levels. At the top of the plan you get an aggregated view. Time on activities also show a summarized time to all project members. Members with write access to the Plan tool can enter and change time estimates and time remaining to complete a particular activity.

        </div>
    </div>
    <div class="row">
        <div class="col-md-3 pull-right">
