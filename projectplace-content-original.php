            <h3>Mobile apps</h3> Boards are now available in the Android app in addition to the iPhone and iPad apps. The app gives easy access to your cards and boards from your phone or tablet so that you can work and talk about it wherever you are. You can download the app from <a href="https://play.google.com/store/apps/details?id=com.projectplace.octopi&amp;hl=en" target="_blank">Google Play</a>.

