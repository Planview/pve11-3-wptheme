            <h2>Improving Kanban Boards</h2>
            <h3>New way of creating cards on boards</h3> Create cards directly in the swim-lane view of the kanban board. Sort the board on activity and create the cards you need for each activity. The cards will also automatically be associated to that activity. Additionally, you can sort the board by assignees and assign as you create cards in the person's swim lane.

