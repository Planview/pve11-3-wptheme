        <div class="col-md-9">
            <h3 id="export-list">Export a list of the document and folder structure</h3> Sometimes you need to keep a list of your projects to track changes or maybe adhere to standards or regulations. You are now able to export a list of your entire folder and document structure to Excel.

            <em>(The export function can be found under the cogwheel menu in Documents.)</em>

            <h3 id="export-portfolio-to-excel">Export contents of a portfolio to Excel</h3> In addition to the existing portfolio report in Word format for stakeholders, this release introduces the capability to export portfolio information to Microsoft Excel for further manipulation.

            <em>(You can find the export function next to the column selector at the top right, in the portfolio view.)</em>


            <h3 id="two-step-verification">Two-step verification</h3> Two-step verification is an excellent way to add an additional level of security to your user accounts. This release provides the ability for account administrators to see which users are using this easy-to-use security measure. This information is also included when exporting people information to an Excel sheet in the toolbar.
