        <div class="col-md-9">
            <h3 id="time-estimates">Time estimates on cards</h3> Time estimates on cards can be visualized in the workload view in order to better assess the workload of a project member.

            <h3 id="workload-trend">Workload trend</h3> Introducing a new way of making sense of workload, the workload trend for each individual presents the number of cards and workload self-assessment over time in a graph. This helps managers find and predict the right workload for an individual.

            <h3 id="assignee-substitution">Assignee substitution</h3> Gerry came down with the flu? Paula got a new position and needs to start immediately? Workload Management in Projectplace can now help you to not only find all the work that is currently assigned to a person, but also to reassign that work to a another team member.
